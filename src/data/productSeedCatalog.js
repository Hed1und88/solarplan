const common = { unit: 'st', price: 0, is_active: true };

function panel(brand, model, power, width, height, voc, vmp, isc, imp, extra = {}) {
  return {
    ...common,
    category: 'solpanel',
    brand,
    model,
    name: `${brand} ${model}`,
    power_watts: power,
    width_mm: width,
    height_mm: height,
    voc_v: voc,
    vmp_v: vmp,
    isc_a: isc,
    imp_a: imp,
    temp_coeff_pmax_percent_c: -0.35,
    temp_coeff_voc_percent_c: -0.27,
    temp_coeff_isc_percent_c: 0.05,
    noct_c: 45,
    bifacial: false,
    description: `Solpanel ${brand} ${model}. Importerad startdata. Kontrollera alltid mot aktuellt datablad före projektering.`,
    ...extra,
  };
}

function inverter(brand, model, acKw, maxDcKw, maxV, startV, mpptMin, mpptMax, mppts, inputA, shortA, extra = {}) {
  return {
    ...common,
    category: 'vaxelriktare',
    brand,
    model,
    name: `${brand} ${model}`,
    power_watts: acKw * 1000,
    max_dc_power_kw: maxDcKw,
    max_dc_voltage_v: maxV,
    startup_voltage_v: startV,
    mppt_voltage_min_v: mpptMin,
    mppt_voltage_max_v: mpptMax,
    nominal_dc_voltage_v: 600,
    mppt_count: mppts,
    strings_per_mppt: 2,
    max_input_current_a: inputA,
    max_short_circuit_current_a: shortA,
    battery_supported: /hybrid|gen24|xh|et|sh|plenticore|rwb|luna/i.test(model),
    phase_type: '3-fas',
    inverter_type: /hybrid|gen24|xh|et|sh|plenticore|rwb/i.test(model) ? 'Hybrid' : 'Strängväxelriktare',
    description: `Växelriktare ${brand} ${model}. Importerad startdata. Kontrollera alltid mot aktuellt datablad före projektering.`,
    ...extra,
  };
}

function battery(brand, model, kwh, extra = {}) {
  return {
    ...common,
    category: 'batteri',
    brand,
    model,
    name: `${brand} ${model}`,
    capacity_kwh: kwh,
    description: `Batterisystem ${brand} ${model}. Importerad startdata. Kontrollera alltid mot aktuellt datablad och kompatibilitetslista.`,
    ...extra,
  };
}

function mounting(brand, model, extra = {}) {
  return {
    ...common,
    category: 'montagesystem',
    brand,
    model,
    name: `${brand} ${model}`,
    description: `Montagesystem ${brand} ${model}. Importerad startdata. Kontrollera alltid mot projekteringsverktyg, lastzoner och tillverkarens montageanvisning.`,
    ...extra,
  };
}

