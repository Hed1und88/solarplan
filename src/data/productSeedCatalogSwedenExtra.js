const common = { unit: 'st', price: 0, is_active: true };

function panel(brand, model, power, width = 1134, height = 1722, voc = 39.5, vmp = 33.0, isc = 14.0, imp = 13.2, extra = {}) {
  return { ...common, category: 'solpanel', brand, model, name: `${brand} ${model}`, power_watts: power, width_mm: width, height_mm: height, voc_v: voc, vmp_v: vmp, isc_a: isc, imp_a: imp, temp_coeff_pmax_percent_c: -0.35, temp_coeff_voc_percent_c: -0.27, temp_coeff_isc_percent_c: 0.05, noct_c: 45, bifacial: !!extra.bifacial, description: `Solpanel ${brand} ${model}. Utökad svensk/Europeisk marknadskatalog. Kontrollera mot aktuellt datablad före projektering.`, ...extra };
}
function inv(brand, model, acKw, maxDcKw, maxV = 1100, startV = 180, mpptMin = 160, mpptMax = 1000, mppts = 2, inputA = 16, shortA = 20, extra = {}) {
  const hybrid = /hybrid|gen24|xh|et|eh|sh|rwb|h3|h1|storage|plenticore/i.test(model);
  return { ...common, category: 'vaxelriktare', brand, model, name: `${brand} ${model}`, power_watts: acKw * 1000, max_dc_power_kw: maxDcKw, max_dc_voltage_v: maxV, startup_voltage_v: startV, mppt_voltage_min_v: mpptMin, mppt_voltage_max_v: mpptMax, nominal_dc_voltage_v: 600, mppt_count: mppts, strings_per_mppt: 2, max_input_current_a: inputA, max_short_circuit_current_a: shortA, battery_supported: hybrid, phase_type: acKw <= 6 ? '1-fas/3-fas beroende modell' : '3-fas', inverter_type: hybrid ? 'Hybrid' : 'Strängväxelriktare', description: `Växelriktare ${brand} ${model}. Utökad svensk/Europeisk marknadskatalog. Kontrollera mot aktuellt datablad före projektering.`, ...extra };
}
function batt(brand, model, kwh, extra = {}) {
  return { ...common, category: 'batteri', brand, model, name: `${brand} ${model}`, capacity_kwh: kwh, description: `Batterisystem ${brand} ${model}. Utökad svensk/Europeisk marknadskatalog. Kontrollera kompatibilitet mot vald växelriktare.`, ...extra };
}
function mount(brand, model, extra = {}) {
  return { ...common, category: 'montagesystem', brand, model, name: `${brand} ${model}`, description: `Montagesystem ${brand} ${model}. Utökad svensk/Europeisk marknadskatalog. Kontrollera snö-/vindlast, taktyp och montageanvisning.`, ...extra };
}

